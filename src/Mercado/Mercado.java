    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jCheckBox1.isSelected()){  
        float vbatata = Float.parseFloat(jTextField1.getText());
        float b = (float) 1.50;
        conta = conta + vbatata*b;
        } else {conta = conta + 0;}
        
        if (jCheckBox2.isSelected()){           
        float vcenoura = Float.parseFloat(jTextField2.getText());
        float c = (float) 2.00;
        conta = conta + vcenoura*c;
        } else {conta = conta + 0;}
        
        if (jCheckBox3.isSelected()){           
        float vcebola = Float.parseFloat(jTextField3.getText());
        float ceb = (float) 3.10;
        conta = conta + vcebola*ceb;
        } else {conta = conta + 0;}
        
        if (jCheckBox4.isSelected()){           
        float vbeterraba = Float.parseFloat(jTextField4.getText());
        float bet = (float) 2.35;
        conta = conta + vbeterraba*bet;
        } else {conta = conta + 0;}
        
        if (jCheckBox5.isSelected()){           
        float vpep = Float.parseFloat(jTextField5.getText());
        float pep = (float) 1.85;
        conta = conta + vpep*pep;
        } else {conta = conta + 0;}
        
        if (jCheckBox6.isSelected()){           
        float vpi = Float.parseFloat(jTextField6.getText());
        float pi = (float) 3.00;
        conta = conta + vpi*pi;
        } else {conta = conta + 0;}
        
        if (jCheckBox7.isSelected()){           
        float vt = Float.parseFloat(jTextField7.getText());
        float t = (float) 3.50;
        conta = conta + vt*t;
        } else {conta = conta + 0;}
        
        
        jLabel11.setText("Total a pagar: R$" + dc.format(conta));
        conta = 0;
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

